      <p class="lead">Sponsoring the IDEAL Lab</p>
      <p>
        The  Intelligent Data Exploration and Analysis Lab (IDEAL) which is directed by <a href="/people/ghosh">Prof. Ghosh</a>, relies heavily on sponsorship from industry. Industrial participation is central to identifying critical real-life  problems for intelligent data analysis and data/web mining. Through this exposure, students can strengthen and find practical applications for their research. Industry benefits by having access to students who are intimately familiar with the problems they are trying to solve and the cutting-edge technologies they are using to do it.
      </p>
      <p>Two levels of industrial sponsorship are common:</p>
      <ul>
        <li>Unrestricted Gift</li>
        <li>Contract Research</li>
      </ul>
      <p>
        In an "unrestricted gift," the University waives overhead charges so that the entire contribution goes to supporting research. Thus, none of it would pay for administrative expenses, building maintenance, or utility costs, for example. However, there is no legal obligation from UT for any deliverables. The cost of sponsoring a student (as Grad. Research Assistant, 20 hrs/week)  is $25,000 (estimated, for 2014-15) for a long semester (4.5 months). This amount includes salary, tuition and fees, and associated research costs (travel, equipment, publication, and so forth). Summer semester sponsorship is $27,000 (40hrs/wk) or $16,000 (20 hrs/wk). Most students however prefer to spend the summer with their sponsoring company as a summer intern. A detailed breakdown of these costs are available upon request.
      </p>
      <p>Academic cycles are:</p>
      <ul>
        <li>Fall: Sept 1 - Jan 15</li>
        <li>Spring: Jan 16 - May 31</li>
        <li>Summer: Jun 1 - Aug 3</li>
      </ul>
      <p>
        [Company Name] is pleased to give an unrestricted gift of $xx,xxx to support the research of Prof. Joydeep Ghosh in the Department of Electrical and Computer Engineering at The University of Texas at Austin, Texas. There is no restriction on the use of these funds.
      </p>
      <p>
        If you prefer to sponsor the lab as a whole, rather than an individual student, you could do so by  signing on as a preferred industrial partner; please contact Prof. Ghosh for details.
      </p>
      <p>
        Contract research is also possible. The terms of the contract must comply with The University of Texas and State of Texas guidelines, including fairly stringent IP issues. Contract research entails about 50% overhead on most items, but accomodates a list of deliverables. It costs approximately $80,000 to support one student plus one month of faculty time for nine months.
      </p>
      <p class="lead">Benefits</p>
      <p>Sponsorship entails the following specific benefits (note that non-sponsors also get access to publications and most software, although usually not as promptly):</p>
      <ul>
        <li>Regular updates on the progress of projects.</li>
        <li>Regular updates on publications from the group.</li>
        <li>Access to all software with the freedom to use, distribute, and modify, as long as The University of Texas receives credit for the development and The University of Texas is not held liable for defects in the software. Most of our software is released under standard gnu-release policies.</li>
        <li>Invitation to project reviews.</li>
        <li>Informal support for the software. While we cannot commit to provide support for everyone, we make every effort to respond quickly to problems that sponsors may have with the software.</li>
        <li>Personal interaction. We will allocate more time to tell you in person what we are doing and to listen to your suggestions about the directions in which we should be going. In fact, we rely on this sort of guidance from our sponsors to make sure our work remains relevant to industrial applications. This guidance can have a strong effect on the direction that the students' research will take.</li>
        <li>Short-term visitors to the campus are welcomed (stays from 1 day to 2 weeks can be arranged).</li>
      </ul>
